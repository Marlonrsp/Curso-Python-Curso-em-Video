# Conversor de Medidas Ex 008

# distancia em metros

me = float(input('Digite uma distancia em metros: '))
cm = me * 100
mm = me * 1000
print('A media de {}m  corresponde a {}cm e {}mm'.format(me,cm,mm))

print ('\n')

# distancia em quilômetros

km = float(input('Digite um valor de KM: '))
m = km * 1000
print('A distancia em {}km corresponde à {}m '.format(km,m))